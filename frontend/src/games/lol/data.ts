// Each champion/spell appears multiple times — once per printing
// printType represents the card "version" the user can filter by
const make = (
  base: Omit<LolCard, 'game' | 'printType' | 'cardNumber'>,
  printings: Array<{ printType: PrintType; cardNumber: string }>
): LolCard[] =>
  printings.map((p) => ({
    ...base,
    game: 'lol' as const,
    printType: p.printType,
    cardNumber: p.cardNumber,
    id: `${base.id}-${p.printType.toLowerCase().replace(/\s/g, '-')}`,
  }));

