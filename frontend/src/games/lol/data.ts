// Flatten into individual card records (one per printing)
export const LOL_CARDS: LolCard[] = BASE_CARDS.flatMap((base) => {
  const printings = PRINT_SCHEDULES[base.name] ?? [{ printType: 'Normal' as PrintType, cardNumber: '???/???' }];
  const baseId = base.name.toLowerCase().replace(/[^a-z0-9]/g, '-');
  return make({ ...base, id: baseId }, printings);
});

